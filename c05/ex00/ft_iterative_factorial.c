int	ft_iterative_factorial(int nb)
{
	int	a;

	a = nb;
	if (nb < 0)
		return (0);
	else if (nb == 0)
		return (1);
	while (a != 1)
	{
		nb = nb * (a - 1);
		a--;
	}
	return (nb);
}
