char	*ft_strupcase(char *str)
{
	int	a;

	a = 0;
	while (str[a] != '\0')
	{
		if (str[a] <= 122 && 97 <= str[a])
			str[a] -= 32;
		else
			a++;
	}
	return (str);
}
