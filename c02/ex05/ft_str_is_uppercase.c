int	ft_str_is_uppercase(char *str)
{
	int	a;

	a = 0;
	while (str[a] != '\0')
	{
		if (!(65 <= str[a] && str[a] <= 90))
		{
			return (0);
		}
		else
			a++;
	}
	return (1);
}
