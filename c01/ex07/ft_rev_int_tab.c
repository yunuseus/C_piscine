void	ft_rev_int_tab(int *tab, int size)
{
	int	a;
	int	swp;

	a = 0;
	while (a < size / 2)
	{
		swp = tab[a];
		tab[a] = tab[size - a - 1];
		tab[size - 1 - a] = swp;
		a++;
	}
}
